import UIKit

public enum Margin {
    static let horizontal: CGFloat = 16
    static let vertical: CGFloat = 16
}

public enum Padding {
    static let horizontal: CGFloat = 4
    static let vertical: CGFloat = 8
}

public enum Color {
    static let background = UIColor.white
    static let text = UIColor.black
    static let textAlternative = UIColor.darkGray
}

public enum Font {
    static let listItem = UIFont.boldSystemFont(ofSize: 16)
    static let title = UIFont.boldSystemFont(ofSize: 24)
    static let subtitle = UIFont.systemFont(ofSize: 18)
    static let description = UIFont.systemFont(ofSize: 16)
}
